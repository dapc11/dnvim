_G.discover_highlights = function()
  local groups = {}
  local output = vim.api.nvim_exec2("highlight", { output = true })
  for line in output.output:gmatch("[^\r\n]+") do
    local group = line:match("^(%S+)")
    if group and not group:match("^%s*$") then
      local hl = vim.api.nvim_get_hl(0, { name = group })
      if next(hl) and (hl.fg or hl.bg) then
        groups[group] = true
      end
    end
  end

  local missing = {}
  for group in pairs(groups) do
    local exists = false
    for line in io.lines(debug.getinfo(1).source:sub(2)) do
      if line:match('"' .. group .. '"') then
        exists = true
        break
      end
    end
    if not exists then
      table.insert(missing, group)
    end
  end

  table.sort(missing)
  local file = vim.fn.stdpath("config") .. "/missing_highlights.txt"
  vim.fn.writefile(missing, file)
  print(string.format("Found %d missing highlights in: %s", #missing, file))
end
