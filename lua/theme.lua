-- Apply brightness to colors (only main syntax colors)
local c = {}
local syntax_colors = { "blue", "green", "yellow", "red", "purple", "orange" }
for key, value in pairs(base_colors) do
  local is_syntax_color = false
  for _, color in ipairs(syntax_colors) do
    if key == color then
      is_syntax_color = true
      break
    end
  end

  if is_syntax_color then
    c[key] = adjust_brightness(value, brightness)
  else
    c[key] = value
  end
end

